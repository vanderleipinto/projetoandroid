package classes;

public class Amigo {
	private String nome;

	public Amigo(String nome) {
		super();
		this.nome = nome;
	}
	
	public Amigo() {
		super();
		this.nome = " ";
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
